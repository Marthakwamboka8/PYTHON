def interest(rate,time,principal):
    answer=(rate*time*principal)
    print("the simple interest is",answer)
# call the function 
interest(0.02,3,234500)

# bmi
def BMI(weight, height):
    answer=(weight/(height*height))
    print("BMI is",answer)
# call the function 
BMI(57,1.6)

# area of a triangle
def area(base,height):
    answer=(0.5*base*height)
    print("The area is", answer)
# call the function 
