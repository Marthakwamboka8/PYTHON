# loop through a list 
languages=["English","Kiswahili","French","German","Spanish","Chinese"]
for language in languages:
    print(language)
countries=["Kenya","Tanzania","Morocco","Egypt","Nigeria","Congo"]
for country in countries:
    print(country)
movies=["F1","Family plan","Stranger things","13 reasons why","his and hers"]
for movie in movies:
    print(movie)
artists=["SZA","Kizz Daniel","Olivia","Mzanzi","Sauti soul"]
for artist in artists:
    print(artist)